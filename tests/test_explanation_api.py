import sys
from pathlib import Path

from fastapi.testclient import TestClient

sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

from backend.db import create_db_and_tables
from backend.main import app
from backend.seed import seed_demo_data


client = TestClient(app)


def setup_module():
    create_db_and_tables()
    seed_demo_data()


def test_concise_explanation_from_direct_pricing_fields():
    response = client.post("/api/explanations/quote", json=_direct_payload())

    assert response.status_code == 200
    data = response.json()
    assert data["product_id"] == 1
    assert data["product_name"] == "Demo A3 Flyer"
    assert data["explanation_source"] == "deterministic_template"
    assert "financially safe" in data["explanation_summary"]
    assert "No price was generated by AI." in data["decision_boundaries"]


def test_manager_explanation_from_direct_pricing_fields():
    payload = _direct_payload()
    payload["explanation_style"] = "manager"
    payload["explanation_audience"] = "manager"
    response = client.post("/api/explanations/quote", json=payload)

    assert response.status_code == 200
    bullets = response.json()["explanation_bullets"]
    assert any("manager should confirm" in bullet for bullet in bullets)
    assert any("manager audience" in bullet for bullet in bullets)


