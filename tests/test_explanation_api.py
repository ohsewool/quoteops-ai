def test_explanation_states_no_approval_or_activation():
    response = client.post("/api/explanations/quote", json=_direct_payload())

    assert response.status_code == 200
    data = response.json()
    combined_text = " ".join(data["explanation_bullets"] + data["decision_boundaries"])
    assert "does not approve or activate" in combined_text
    assert "No approval decision was made by AI." in combined_text


def test_explanation_from_approval_request_snapshot():
    approval = client.post(
        "/api/approval-requests",
        json={
            "product_id": 1,
            "quantity": 10,
            "proposed_unit_price": 4000,
            "minimum_margin_rate": 0.3,
        },
    )
    assert approval.status_code == 201

    response = client.post(
        "/api/explanations/quote",
        json={
            "approval_request_id": approval.json()["id"],
            "explanation_style": "detailed",
        },
    )

    assert response.status_code == 200
    data = response.json()
    assert data["proposed_unit_price"] == 4000.0
    assert data["validation_status"] == "passed"
    assert data["risk_level"] == "low"


def test_openapi_includes_quote_explanation_path():
    response = client.get("/openapi.json")

    assert response.status_code == 200
    assert "/api/explanations/quote" in response.json()["paths"]


